package mx.com.gnp.demo.dto;

public class Kcirinte {

    public Kcirip06 datinter;

    public String intipoop;

    public Kcirinte() {
        super();
        datinter = new Kcirip06();

        intipoop = new String();

    }

    /***************************************************************************
     * Metodos Get
     **************************************************************************/
    public Kcirip06 getDatinter() {
        return this.datinter;
    }

    public String getIntipoop() {
        return this.intipoop;
    }

    /***************************************************************************
     * Metodos Set
     **************************************************************************/
    public void setDatinter(Kcirip06 datinter) {
        this.datinter = datinter;
    }

    public void setIntipoop(String intipoop) {
        this.intipoop = intipoop;
    }

}